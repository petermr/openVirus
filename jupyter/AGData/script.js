function getDonutData(group, column) {
	function sum(numbers) {
		return _.reduce(
			numbers,
			function (result, current) {
				return result + 1;
			},
			0
		);
	}
	var result = _.chain(getChartData())
		.groupBy(group)
		.map(function (value, key) {
			return {
				label: key,
				value: sum(_.pluck(value, column))
			};
		})
		.value();

	return result;
}

